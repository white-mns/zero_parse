package NumCode;
use Encode 'from_to';

#-----------------------------------#
#
#        エンコード
#            引数はutf-8とする
#-----------------------------------#
sub Encode{
    (my $content) = @_;
    $content =~ s/&#/"<<>>"/ge;
    $content =~ s/"ー"/"<<1>>"/ge;
    $content =~ s/"～"/"<<2>>"/ge;
    return $content;
}

#-----------------------------------#
#
#        エンコード
#            引数はutf-8とする
#-----------------------------------#
sub EncodeEscape{
    (my $content) = @_;
    $content =~ s/&copy;/"&amp;copy;"/ge;
    $content =~ s/&reg;/"&amp;reg;"/ge;
    $content =~ s/&#/"&amp;#"/ge;
    return $content;
}

#-----------------------------------#
#
#        デコード
#
#-----------------------------------#
